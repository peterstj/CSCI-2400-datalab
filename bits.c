/* 
 * evenBits - return word with all even-numbered bits set to 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 1
 */
int evenBits(void) {
    /*move eight bits then mask with exclusive or, repeat 4 times*/
    return (((((0x55<<8)^0x55)<<8)^0x55)<<8)^0x55;
}
/* 
 * minusOne - return a value of -1 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int minusOne(void) {
    /*return the opposite of all zeros which is equal to -1*/
    return ~0x0;
}
/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
    /*move LSB to MSB then use switch to fill every postion with LSB*/
    return (x<<31)>>31;
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
    /*check if negative or positive w/ a, if -ve add 2^n - 1 to x with b*/
    int a = x >> 31;
    int b = (1<<n)+(~0);
    return ((~a) & (x>>n)) | (a & ((x+b)>>n));
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
    /*move the byte to spot 0 then mask with 0xff*/
    return (x>>(n<<3)) & 0xff;
}
/* 
 * anyOddBit - return 1 if any odd-numbered bit in word set to 1
 *   Examples anyOddBit(0x5) = 0, anyOddBit(0x7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int anyOddBit(int x) {
    /*create a mask with ones only in odd spots then double negate value to 
     *either give one or zero*/
    int a = ((((((0xaa<<8)^0xaa)<<8)^0xaa)<<8)^0xaa);
    return !(!(a&x));
}
/* 
 * isNegative - return 1 if x < 0, return 0 otherwise 
 *   Example: isNegative(-1) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNegative(int x) {
    /*if x is negative masks -1 with 1, if positive masks 0 with 1*/
    int a = x>>31;
    return (a & 0x01);
}
/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
    /*use a and b to figure out if x is in range from 0x30 to 0x40 then return 
     *one if it is*/
    int a = x+(~0x40+1);
    int b = x+(~0x30+1); 
    return (0x1&(a^b));
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int fitsBits(int x, int n) {
    /*determine if x is +ve or -ve, compare with x without the given number of
     *bits*/
    int a = x>>31;
    int b = x>>(n+(~0));
    int c = !(a^b);
    return c;
}
/* 
 * subOK - Determine if can compute x-y without overflow
 *   Example: subOK(0x80000000,0x80000000) = 1,
 *            subOK(0x80000000,0x70000000) = 0, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int subOK(int x, int y) {
    /*check for change in sign with (x-y) and x, then with x and y. If both 
     *change return 0*/
    int a = (x + (~y+1));
    int b = (x^a)>>31;
    int c = (x^y)>>31;
    return !(b&c);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
    /*make a = to either 1 or zero, then create a mask with b, then return a y
     *or z based on mask*/
    int a = !(!x);
    int b = (a<<31)>>31;
    return ((b & y) | (~b & z));
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
    /*a checks if x is negative, b converts x to not x if its negative, c checks
     *if x is zero, d checks if x is -1, then use binary search to count how many
     *bits with pos*/
    int pos = 0;
    int a = x>>31;
    int b = (a&(~x))|((~a)&x);
    int c = ((!!x)<<31)>>31;
    int d = x^(~0x0);
    int e = ((!!d)<<31)>>31;
    int bits;
    pos = ((!!(b>>16))<<8)>>4;
    pos += ((!!(b>>(pos+8)))<<4)>>1;
    pos += (!!(b>>(pos+4)))<<2;
    pos += (!!(b>>(pos+2)))<<1;
    pos += (!!(b>>(pos+1)))<<0;
    pos += 1;
    pos = pos&c;
    bits = pos+1;
    return (bits & e)|(0x1&(~e));
}
/* 
 * isNonZero - Check whether x is nonzero using
 *              the legal operators except !
 *   Examples: isNonZero(3) = 1, isNonZero(0) = 0
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4 
 */
int isNonZero(int x) {
    /*checks MSB for a one with x and -x, returns zero if MSB=0 for both*/
    int a =(~x+1);
    int b = 1<<31;
    int c = ((a&b)|(x&b))>>31;
    return c&1;
}
/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
    /*a determines if x is negative, then function returns the opposite if it 
     *is or not*/
    int a = x>>31;
    return (a&(~x+1))|((~a)&(x));
}
/*
 * isPower2 - returns 1 if x is a power of 2, and 0 otherwise
 *   Examples: isPower2(5) = 0, isPower2(8) = 1, isPower2(0) = 0
 *   Note that no negative number is a power of 2.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int isPower2(int x) {
    /*x and x-1 returns 0 if x is a power 2, and c returns 0 if x is 0, and d 
     *checks if there is a one in the 31 spot.*/
    int a = x+(~0);
    int b = x&a;
    int c = ((!!x)<<31)>>31;
    int d = x>>31;
    return ((!b)&c)&(!d);
}
